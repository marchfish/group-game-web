    <h4>
1.个人信息的定义与范围
    </h4>

    <p>
        1.1 个人信息，是指以电子或者其他方式记录的能够单独或者与其他信息结合识别自然人个人身份的各种信息，在本隐私政策中包括：姓名、手机号码、用户密码、身份证号码、身份证或其他身份证明、照片、性别、年龄、驾驶证、常用地址、通讯地址、位置信息、行程信息、录音录像、订单信息及交易状态、支付信息、提现记录、银行卡号、评价信息、微圈信息、设备信息、IP地址。
    </p>

    <p>
        1.2 个人敏感信息，是指一旦泄露、非法提供或滥用可能危害人身和财产安全，极易导致个人名誉、身心健康受到损害或歧视性待遇等的个人信息，在本隐
    </p>

    <p>
        私政策中包括：手机号码、用户密码、身份证或其他身份证明、驾驶证、位置信息、录音录像、行程信息、支付信息、提现记录、银行卡号。
    </p>

    <p>
        1.3 匿名化处理后的信息，即通过对个人信息的技术处理，使得个人信息主体无法被识别，且处理后不能被复原的信息，匿名化处理后的信息不属于个人信息。
    </p>

    <p>
        1.4其他无法识别特定自然人身份或者反映特定自然人活动情况的信息不属于个人信息。
    </p>

