    <h4>
        2.个人信息的收集
    </h4>

    <p>
        2.1 找跑跑平台业务功能分为核心业务功能、附加业务功能，我们将详细向您说明各业务功能所收集的个人信息以及收集方法、目的，拒绝提供的影响等。业务功能具体包括：
    </p>

    <p>
        2.1.1 核心业务功能，包括创建账号并完善资料，找跑跑服务，交易支付，数据分析及产品开发。
    </p>

    <p>
        2.1.2 附加业务功能，包括用户评价，客服服务及运营活动。
    </p>

    <p>
        2.2 您使用找跑跑平台服务前，需要在找跑跑平台创建账号并完善个人资料。通过用户主动提供的方式，我们会收集您的以下信息：
    </p>

    <p>
        2.2.1 您需要向我们提供您的手机号码，并设置您在找跑跑平台的用户密码。我们以此识别您在找跑跑平台的网络身份，并为您提供找跑跑平台服务。如果您拒绝提供手机号码，找跑跑平台将无法为您创建账号并提供服务。
    </p>

    <p>
        2.2.2 您可以选择向我们提供照片、姓名、身份证号、性别、年龄信息。您可以在个人账户资料中选择是否填写或授权找跑跑获取上述信息。
    </p>

    <p>
        2.2.3 如果您是骑手用户，必须向我们提供姓名、身份证或其他身份证明、面部识别特征信息、所在城市、家庭地址信息。我们收集上述信息，是基于法律法规要求以及保护平台用户人身财产安全之需要。如果您拒绝提供上述信息，将无法作为找跑跑平台的骑手用户，向其他用户提供服务。
    </p>

    <p>
        2.3 您使用找跑跑平台为其他用户服务时，我们会收集您的以下信息：
    </p>

    <p>
        2.3.1 位置信息，在您首次使用找跑跑平台前，我们会提示您选择是否允许找跑跑平台获取以及何种情况下可以获取您的位置信息。在您使用找跑跑平台的过程中，您可以随时通过您的设备设置功能，选择是否允许找跑跑平台获取以及何种情况下可以获取您的位置信息。获得您的允许后，我们会通过IP地址、GPS以及能够提供相关信息的其他传感器来获取您的位置信息。
    </p>

    <p>
        2.3.2 行程信息，我们将通过记录用户行为收集您的出发地、到达地、行踪轨迹、时长及里程数信息。我们收集上述信息，是基于法律法规要求以及保护您的人身财产安全、依照平台规则处理用户纠纷之需要。如果您拒绝提供行程信息，将导致平台无法为您提供服务。
    </p>

    <p>
        2.3.3 通话信息，您通过找跑跑平台接打电话的通话内容、通话时长、主被叫手机号码信息，以及您通过找跑跑平台发送的文字信息内容。为更好保护用户安全，找跑跑平台不直接向用户提供其他用户的手机号码等联系方式，因此用户只能通过找跑跑平台进行通话或信息沟通。我们通过通话录音的方式留存上述信息，以便用户发生纠纷或安全事故时，核实事实并处理投诉。如果您拒绝我们获取您的通话信息，可能导致您无法使用找跑跑平台的通话及信息功能。
    </p>

    <p>
        2.3.4 语音信息，您使用找跑跑平台在服务过程中可能存在发布需求或与骑手联系的语音信息。我们留存上述信息，是基于地方性法律法规要求，或用于用户发生纠纷时，核实事实并处理投诉。
    </p>

    <p>
        2.3.5 订单信息及交易状态。我们将记录您的订单信息及交易状态。我们收集上述信息是基于法律法规要求以及保护您的人身财产安全、依照平台规则处理用户纠纷之需要。
    </p>

    <p>
        2.4 您使用找跑跑平台服务时，为保护用户财产权利，维护找跑跑平台交易秩序，我们会要求您提供姓名、身份证号、面部识别特征、预付信息，并记录您的订单信息及交易状态。
    </p>

    <p>
        2.5 您在找跑跑平台进行交易支付时，如果您使用第三方支付功能，找跑跑除获取您的支付工具及订单支付状态外，不会获取其他个人信息。如果您使用找跑跑钱包功能，您需要设置您在找跑跑平台的支付密码，我们将记录您的余额、支付记录、提现记录以及银行卡号。我们收集上述信息以核实用户是否按约付费，维护平台交易秩序，并按照平台规则处理用户纠纷。如果您拒绝我们获取您的银
    </p>

    <p>
        行卡号，您将无法使用找跑跑钱包的提现功能，但不影响您使用找跑跑平台其他服务。
    </p>

    <p>
        2.6 您可以在找跑跑平台对其他用户进行星级和文字评价。
    </p>

    <p>
        2.7 当您与找跑跑客服团队联系时，我们会以通话录音及人工录入的方式记录您的通话信息、您提出的意见或建议、您提供的姓名、联系方式，以便我们处理纠纷、解决问题或向您反馈结果。如果您拒绝我们收集此类信息，会导致您无法拨打我们的客服电话或客服团队无法将处理结果反馈到您，但不影响您使用找跑跑平台核心业务功能。
    </p>

    <p>
        2.8 您参加我们的运营活动时，我们需要您提供某些信息，可能会包含您的姓名、照片、通讯地址、联系方式、银行卡号。这些信息可以帮助我们对活动结果进行公示，对活动进行后续宣传，与您取得联系，向您发放礼品或转账。如果您拒绝提供此类信息，可能导致无法参加相应活动，或无法收到礼品、转账，但不会影响您使用找跑跑平台核心业务功能。
    </p>

    <p>
        2.9 为开展数据分析和更好改善找跑跑平台服务，我们会收集您的日志信息，包括检索内容、IP地址、设备信息（包括设备型号、设备识别码、操作系统、分辨率、电信运营商）、位置信息、订单信息，作出特征模型并进行用户画像，以为您提供更加个性化、便捷的服务。我们会使用浏览器网络存储机制和应用程序数据缓存，在您的设备上收集信息并进行本地存储。
